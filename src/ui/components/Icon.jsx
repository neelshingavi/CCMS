export function Icon({
    name,
    size = 'md',
    color,
    className = '',
    ...props
}) {
    const iconPath = icons[name];

    if (!iconPath) {
        console.warn(`Icon "${name}" not found`);
        return null;
    }

    const classes = [
        'icon',
        `icon--${size}`,
        color && `icon--${color}`,
        name === 'spinner' && 'icon--spin',
        className
    ].filter(Boolean).join(' ');

    return (
        <svg
            className={classes}
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            aria-hidden="true"
            {...props}
        >
            {iconPath}
        </svg>
    );
}

